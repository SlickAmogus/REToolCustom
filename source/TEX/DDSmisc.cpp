bool IsBlockCompressed(int format)
{
	//BC1-BC5: DXGI formats 70-84, BC6H-BC7: formats 94-99
	return (format >= 70 && format <= 84) || (format >= 94 && format <= 99);
}

int GetBlockSizeBytes(int format)
{
	if(format >= 70 && format <= 72) return 8;  //BC1
	if(format >= 73 && format <= 78) return 16; //BC2, BC3
	if(format >= 79 && format <= 81) return 8;  //BC4
	if(format >= 82 && format <= 84) return 16; //BC5
	if(format >= 94 && format <= 96) return 16; //BC6H
	if(format >= 97 && format <= 99) return 16; //BC7
	return 0;
}

int CalculateMipPitch(int width, int format, bool padToMinimum)
{
	int pitch;
	int blockSizeBytes = GetBlockSizeBytes(format);

	if(blockSizeBytes > 0) //Block compressed
	{
		int blocksW = (width + 3) / 4;
		if(blocksW < 1) blocksW = 1;
		pitch = blocksW * blockSizeBytes;
	}
	else //Uncompressed
	{
		int bpp = BitsPerPixelDX10Format(format);
		if(bpp <= 0) bpp = 32;
		pitch = width * (bpp / 8);
	}

	if(padToMinimum && pitch < RE9_MIN_PITCH)
		pitch = RE9_MIN_PITCH;

	return pitch;
}

int CalculateMipDataSize(int width, int height, int format, bool padToMinimum)
{
	int pitch = CalculateMipPitch(width, format, padToMinimum);
	int rows;

	if(IsBlockCompressed(format))
	{
		rows = (height + 3) / 4;
		if(rows < 1) rows = 1;
	}
	else
	{
		rows = height;
		if(rows < 1) rows = 1;
	}

	return pitch * rows;
}

int CalculateNaturalMipDataSize(int width, int height, int format)
{
	return CalculateMipDataSize(width, height, format, false);
}

